typedef enum {
  IMPLICIT = 0, // doesn't require operand 
  ACCUMLATOR, // works on the accumlator
  IMMEDIATE, // Just a byte value stored with the instruction
  ZEROPAGE, // same as absoulute but only works with $00 - $FF
  ZPX, // X or y is just added to the zero page address 
  ZPY,
  RELATIVE, // just a relative address for instance bne *+4 add's 4 byte to the PC when branching
  ABSOLUTE, // Just stores an absoulute address 
  ABSX, // this just add x or y to an absoulute address
  ABSY, 
  INDIRECT, // jmp (ptr) jumps to address stored in this memory location
  INDEXIND,  // lda ($40,X) = addr + x = ptr would load pointer from $41
  INDINDEX,  // lda ($40),y = ptr[y]  if $40 was the low byte of pointer $123 it would load from $124
}addrmode_t;
